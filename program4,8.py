#write a program that inputs a character and display wheather it is 
#vowel or not.

c = input("please enter a character: ")

if c == 'a' or c == 'e' or c == 'i' or c == 'o' or c == 'u' or c == 'A' or c == 'E' or c == 'I' or c == 'O' or c == 'U':
    print(c, "is a vowel")  
else:
    print(c, "is a consonant") 